var numero; //Declaración de la variable

numero = 5; //Cuando se le asigna un valor, inicialización de la variable

document.write(numero);


//El Hoisting: primero se escribe el valor de la var( inicialización) y luego se declara, toma la declaración y la coloca al principio del documento js. Js coloca todas las declaraciones de las variables al principio del documento o de la función.
//Hosting, o elevación es el comportamiento por defecto de JavaScript de “mover declaraciones al principio” del código.